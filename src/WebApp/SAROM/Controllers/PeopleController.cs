using Microsoft.AspNetCore.Mvc;
using Microsoft.EntityFrameworkCore;
using SAROM.Models;
using System.Linq;
using System.Threading.Tasks;

namespace SAROM.Controllers
{
  public class PeopleController : Controller
  {
    private readonly OperationContext _context;

    public PeopleController(OperationContext context)
    {
      _context = context;
    }

    // GET: People
    public async Task<IActionResult> Index()
    {
      return View(await _context.Person.ToListAsync());
    }

    // GET: People/Details/5
    public async Task<IActionResult> Details(string id)
    {
      if (id == null)
      {
        return NotFound();
      }

      var person = await _context.Person
          .FirstOrDefaultAsync(m => m.Id == id);
      if (person == null)
      {
        return NotFound();
      }

      return View(person);
    }

    // GET: People/Create
    public IActionResult Create()
    {
      return View();
    }

    // POST: People/Create
    // To protect from overposting attacks, please enable the specific properties you want to bind to, for
    // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
    [HttpPost]
    [ValidateAntiForgeryToken]
    public async Task<IActionResult> Create([Bind("Dog,Id,Name,Type")] Person person)
    {
      if (ModelState.IsValid)
      {
        _context.Add(person);
        await _context.SaveChangesAsync();
        return RedirectToAction(nameof(Index));
      }
      return View(person);
    }

    // GET: People/Edit/5
    public async Task<IActionResult> Edit(string id)
    {
      if (id == null)
      {
        return NotFound();
      }

      var person = await _context.Person.FindAsync(id);
      if (person == null)
      {
        return NotFound();
      }
      return View(person);
    }

    // POST: People/Edit/5
    // To protect from overposting attacks, please enable the specific properties you want to bind to, for
    // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
    [HttpPost]
    [ValidateAntiForgeryToken]
    public async Task<IActionResult> Edit(string id, [Bind("Dog,Id,Name,Type")] Person person)
    {
      if (id != person.Id)
      {
        return NotFound();
      }

      if (ModelState.IsValid)
      {
        try
        {
          _context.Update(person);
          await _context.SaveChangesAsync();
        }
        catch (DbUpdateConcurrencyException)
        {
          if (!PersonExists(person.Id))
          {
            return NotFound();
          }
          else
          {
            throw;
          }
        }
        return RedirectToAction(nameof(Index));
      }
      return View(person);
    }

    // GET: People/Delete/5
    public async Task<IActionResult> Delete(string id)
    {
      if (id == null)
      {
        return NotFound();
      }

      var person = await _context.Person
          .FirstOrDefaultAsync(m => m.Id == id);
      if (person == null)
      {
        return NotFound();
      }

      return View(person);
    }

    // POST: People/Delete/5
    [HttpPost, ActionName("Delete")]
    [ValidateAntiForgeryToken]
    public async Task<IActionResult> DeleteConfirmed(string id)
    {
      var person = await _context.Person.FindAsync(id);
      _context.Person.Remove(person);
      await _context.SaveChangesAsync();
      return RedirectToAction(nameof(Index));
    }

    private bool PersonExists(string id)
    {
      return _context.Person.Any(e => e.Id == id);
    }
  }
}